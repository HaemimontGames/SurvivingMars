GlobalVar("g_UniqueHumanNames", false)
GlobalVar("g_UniqueMachineNames", false)
function OnMsg.NewMapLoaded()
	g_UniqueHumanNames = {}
	for nation, data in pairs(HumanNames) do
		g_UniqueHumanNames[nation] = data.Unique and table.copy(data.Unique, "deep")  or {}
	end
	g_UniqueMachineNames = {}
	for class_type, data in pairs(MachineNames) do
		g_UniqueMachineNames[class_type] = data.Unique and table.copy(data.Unique, "deep")  or {}
	end
end

function NameUnit(unit)
	local sponsor_id = g_CurrentMissionParams.idMissionSponsor
	local nation = unit.birthplace or "Mars"
	local gender = unit.entity_gender
	-- choose name from the opposite gender if gender is 'Other'
	if unit.gender~=gender and Random(0, 100)<30 then
		gender = gender=="Male" and "Female" or "Male"
	end
	
	if Random(0, 100, "unique_name_chance") < 1 then
		local custom_names = g_UniqueHumanNames[nation] and g_UniqueHumanNames[nation][gender]
		if custom_names and #custom_names > 0 then
			local idx = Random(1, #custom_names, "custom_name")
			local name = custom_names[idx]
			table.remove(custom_names, idx)
			unit.name = name
			return name
		end
	end
	local is_newborn = unit.age==0
	-- try for mars name
	if is_newborn then
		local rand = Random(1,100, "unit_name")
		local sol = UICity.day
		if HumanNames.Mars and sol>= const.FullTransitionToMarsNames or rand<MulDivRound(sol, 100, const.FullTransitionToMarsNames) then
			nation="Mars"
		end
	end	
	local names = HumanNames[nation]
	if not names then return "" end
	local gender_names = names[gender]
	local first = gender_names.First or gender_names
	local family = gender_names.Family or names.Family
	local name = first[Random(1, #first, "unit_name")] .. " " .. family[Random(1, #family, "unit_name")]
	unit.name = name	 
end

GlobalVar("g_UsedMachineNames", {})
function GenerateMachineName(machine_type, does_not_use)
	g_UniqueMachineNames = g_UniqueMachineNames or {}
	if AsyncRand(100) < 1 then
		local custom_names = g_UniqueMachineNames[machine_type] or empty_table
		local name, idx = table.rand(custom_names)
		if name then
			if not does_not_use then
				table.remove(custom_names, idx)
			end
			return name
		end
	end
	local sponsor_id = g_CurrentMissionParams.idMissionSponsor
	local name_lists = MachineNames[machine_type] or empty_table
	local names = name_lists[sponsor_id] or empty_table
	g_UsedMachineNames = g_UsedMachineNames or {}
	g_UsedMachineNames[machine_type] = g_UsedMachineNames[machine_type] or {}
	local used = g_UsedMachineNames[machine_type]
	local name = table.rand(names) or name_lists.default or ResolveDisplayName(machine_type)
	local translated_name = _InternalTranslate(name)
	name = translated_name
	local entry = used[translated_name]
	entry = entry and entry + 1 or 1
	name = translated_name .. " #"..(entry)
	if not does_not_use then
		used[translated_name] = entry or 1
	end
	return name, translated_name
end

function MarkNameAsUsed(machine_type, name)
	if not name then 
		return 
	end
	if table.remove_entry(g_UniqueMachineNames[machine_type], name) then
		return
	end	
	g_UsedMachineNames[machine_type] = g_UsedMachineNames[machine_type] or {}
	local used = g_UsedMachineNames[machine_type]
	local entry = used[name]
	if entry then
		entry = entry + 1
	end
	used[name] = entry or 1
end

function GenerateRocketName(mark)
	local name, base_name = GenerateMachineName("Rocket", mark)
	if UICity then
		for _, rocket in ipairs(UICity.labels.SupplyRocket or empty_table) do
			assert(rocket.name ~= name)
		end
	end
	return name, base_name
end